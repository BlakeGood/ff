
public class tightends {
	
	private String name;
	private int ranking;
	
	public tightends(String n, int r)
	{
		name = n;
		ranking = r;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getRanking() {
		return ranking;
	}

	public void setRanking(int ranking) {
		this.ranking = ranking;
	}
	
	

}



